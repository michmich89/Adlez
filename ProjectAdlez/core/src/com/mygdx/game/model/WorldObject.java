package com.mygdx.game.model;

public abstract class WorldObject {

    private float posX;
    private float posY;

    public float getPosX() {
        return posX;
    }

    public void setPosX(float x) {
        this.posX = x;
    }

    public float getPosY() {
        return posX;
    }

    public void setPosY(float y) {
        this.posY = y;
    }

}
